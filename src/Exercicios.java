public class Exercicios {
    public static void main(String[] args) {
        System.out.println("Conversor de Celsius -> Fahrenheit");

        // variavel para recebe a temperatura e o conversor é para calcular a temperatura
        float temperatura;
        double conversor;

        // entrada da informação e conversão da temperatura informada
        temperatura = 36;
        conversor = (temperatura * 1.8) + 32;

        // apresenta o resultado da conversão
        System.out.println(conversor);

    }
}
